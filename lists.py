
# Lists: ordered, mutable, allows duplicate elements
myList = [
  "banana", "cherry", "apple", "banana", 2, True
]
print(myList)

# create new empty list with list() function
myList2 = list()
print(myList2)

# split string into list
text = "Hello There!"
myList3 = list(text)
print(myList3)

test2 = ["Hello", "there", ["Hey"]]
myList4 = list(test2)
print(myList4)

